        print(f"Найденный x: {x}")

        # Проверяем корректность
        if verify_dlog(p, a, b, x):
            print(f"Проверка пройдена: {a}^{x} ≡ {b} (mod {p})")
        else:
            print(f"Ошибка: {a}^{x} ≠ {b} (mod {p})")

