import math
import random

def нод(a, b):
    """Вычисляет НОД чисел a и b"""
    while b != 0:
        a, b = b, a % b
    return a

def поллард_ро(n, c=1, функция=None):
    """
    p-метод Полларда для нахождения нетривиального делителя n

    Параметры:
    n: число для факторизации
    c: начальное значение (по умолчанию 1)
    функция: используемая функция f (по умолчанию f(x) = x² + 5 mod n)
    """
    if n % 2 == 0:
        return 2

    if функция is None:
        функция = lambda x: (x*x + 5) % n

    a = c
    b = c
    d = 1

    print(f"{'i':<3} {'a':<10} {'b':<10} {'|a-b|':<12} {'НОД':<10}")
    print("-" * 50)

    i = 0
    while d == 1:
        a = функция(a)
        b = функция(функция(b))
        d = нод(abs(a - b), n)

        print(f"{i:<3} {a:<10} {b:<10} {abs(a-b):<12} {d:<10}")
        i += 1

        if d == n:
            return None  # Неудача, попробовать с другими параметрами

    return d

def ферма_факторизация(n):
    """
    Метод квадратов Ферма для факторизации n
    Находит представление n = s² - t²
    """
    if n % 2 == 0:
        return 2

    x = math.isqrt(n)
    if x*x == n:
        return x

    x += 1
    while True:
        y2 = x*x - n
        y = math.isqrt(y2)
        if y*y == y2:
            # n = (x-y)(x+y)
            return x - y
        x += 1

