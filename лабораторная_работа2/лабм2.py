def vigenere_cipher(text: str, key: str) -> str:
    text = text.replace(' ', '').lower()
    key = key.lower()
    alphabet = list("абвгдеёжзийклмнопрстуфхцчшщъыьэюя")
    n = len(alphabet)

    # Étendre la clé à la longueur du texte
    extended_key = []
    key_chars = list(key)
    for i in range(len(text)):
        extended_key.append(key_chars[i % len(key)])

    ciphertext_chars = []
    for i, char in enumerate(text):
        if char in alphabet:
            char_idx = alphabet.index(char)
            key_char = extended_key[i]
            key_idx = alphabet.index(key_char)
            new_idx = (char_idx + key_idx) % n
            ciphertext_chars.append(alphabet[new_idx])
        else:
            ciphertext_chars.append(char)

    return ''.join(ciphertext_chars).upper()


def vigenere_decipher(ciphertext: str, key: str) -> str:
    ciphertext = ciphertext.lower()
    key = key.lower()
    alphabet = list("абвгдеёжзийклмнопрстуфхцчшщъыьэюя")
    n = len(alphabet)

    # Étendre la clé à la longueur du texte chiffré
    extended_key = []
    key_chars = list(key)
    for i in range(len(ciphertext)):
        extended_key.append(key_chars[i % len(key)])

    plaintext_chars = []
    for i, char in enumerate(ciphertext):
        if char in alphabet:
            char_idx = alphabet.index(char)
            key_char = extended_key[i]
            key_idx = alphabet.index(key_char)
            new_idx = (char_idx - key_idx + n) % n
            plaintext_chars.append(alphabet[new_idx])
        else:
            plaintext_chars.append(char)

    return ''.join(plaintext_chars)


