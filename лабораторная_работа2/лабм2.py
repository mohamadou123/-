"""Реализация шифрования с помощью решеток"""

def rot90(matrix, k=1):
    """Rotation de la matrice de 90° k fois."""
    result = matrix.copy()
    for _ in range(k % 4):
        result = [list(reversed(col)) for col in zip(*result)]
    return result


def generate_fleisner_grill(k: int):
    """Génération de la grille de Fleisner."""
    # Créer un carré de base avec des nombres de 1 à k^2
    base = [[(i * k + j + 1) for j in range(k)] for i in range(k)]

    # Créer un grand carré 2k x 2k
    big_square = [[0 for _ in range(2 * k)] for _ in range(2 * k)]

    # Remplir avec les carrés de base tournés
    for i in range(k):
        for j in range(k):
            big_square[i][j] = base[i][j]
            big_square[i][j + k] = rot90(base, 1)[i][j]
            big_square[i + k][j + k] = rot90(base, 2)[i][j]
            big_square[i + k][j] = rot90(base, 3)[i][j]

    return big_square


def fleisner_encrypt(text: str, k: int, password: str) -> str:
    text = text.replace(' ', '').lower()
    password = password.lower()
    required_length = 4 * (k ** 2)

    # Vérification de la longueur du texte
    if len(text) != required_length:
        raise ValueError(f"La longueur du texte doit être {required_length}, reçu {len(text)}")

    # Génération de la grille
    grill = generate_fleisner_grill(k)

    # Créer une table pour l'écriture
    table = [['' for _ in range(2 * k)] for _ in range(2 * k)]
    text_chars = list(text)
    text_index = 0

    # Écriture avec rotation de la grille (4 rotations)
    for rotation in range(4):
        rotated_grill = rot90(grill, rotation)

        # Recherche des cellules pour l'écriture (valeurs de 1 à k^2)
        for i in range(2 * k):
            for j in range(2 * k):
                if 1 <= rotated_grill[i][j] <= k ** 2:
                    if text_index < len(text_chars):
                        table[i][j] = text_chars[text_index]
                        text_index += 1

    # Trier les colonnes par ordre alphabétique des lettres du mot de passe
    # Répéter le mot de passe pour couvrir toutes les colonnes
    extended_password = (password * (2 * k))[:2 * k]
    sorted_indices = sorted(range(2 * k), key=lambda i: extended_password[i])

    ciphertext_chars = []
    for col in sorted_indices:
        for i in range(2 * k):
            if table[i][col] != '':  # Éviter les cellules vides
                ciphertext_chars.append(table[i][col])

    return ''.join(ciphertext_chars).upper()


