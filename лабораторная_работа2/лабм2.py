def columnar_transposition(text: str, password: str) -> str:
    text = text.replace(' ', '').lower()
    password = password.lower()
    n = len(password)
    m = len(text) // n + (1 if len(text) % n != 0 else 0)

    # Remplir le tableau ligne par ligne
    table = [['' for _ in range(n)] for _ in range(m)]
    for i, char in enumerate(text):
        row = i // n
        col = i % n
        table[row][col] = char

    # Trier les colonnes par ordre alphabétique du mot de passe
    sorted_indices = sorted(range(n), key=lambda i: password[i])

    # Lire colonne par colonne dans l'ordre trié
    ciphertext_chars = []
    for col in sorted_indices:
        for row in range(m):
            if table[row][col] != '':
                ciphertext_chars.append(table[row][col])

    return ''.join(ciphertext_chars).upper()


def columnar_decryption(ciphertext: str, password: str) -> str:
    ciphertext = ciphertext.lower()
    password = password.lower()
    n = len(password)
    m = len(ciphertext) // n + (1 if len(ciphertext) % n != 0 else 0)

    # Obtenir l'ordre des colonnes lors du chiffrement
    sorted_indices = sorted(range(n), key=lambda i: password[i])

    # Créer un tableau vide
    table = [['' for _ in range(n)] for _ in range(m)]

    # Calculer le nombre de caractères par colonne
    chars_per_col = [m] * n
    remainder = len(ciphertext) % n
    for i in range(remainder):
        chars_per_col[i] -= 1

    # Remplir le tableau colonne par colonne dans l'ordre trié
    text_idx = 0
    for col_index, original_col in enumerate(sorted_indices):
        col_length = chars_per_col[col_index]
        for row in range(col_length):
            table[row][original_col] = ciphertext[text_idx]
            text_idx += 1

    # Restaurer le texte en lisant ligne par ligne
    plaintext_chars = []
    for row in range(m):
        for col in range(n):
            if table[row][col] != '':
                plaintext_chars.append(table[row][col])

    return ''.join(plaintext_chars)


