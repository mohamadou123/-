def euclidean_algorithm(a: int, b: int) -> int:
    # Проверка, что b не равно 0
    if b == 0:
        raise ValueError("b не должно быть равно 0")

    # Шаг 1: Инициализация
    r_prev, r_curr = a, b

    # Шаг 2-3: Последовательное деление с остатком
    while True:
        # Находим остаток от деления
        r_next = r_prev % r_curr

        # Если остаток равен 0, возвращаем текущий делитель
        if r_next == 0:
            return abs(r_curr)  # Возвращаем абсолютное значение

        # Обновляем значения для следующей операции
        r_prev, r_curr = r_curr, r_next

