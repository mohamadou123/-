def extended_euclidean_algorithm(a: int, b: int) -> tuple[int, int, int]:
    # Проверка, что b не равно 0
    if b == 0:
        raise ValueError("b не должно быть равно 0")

    # Инициализация
    r_prev, r_curr = a, b
    x_prev, x_curr = 1, 0
    y_prev, y_curr = 0, 1

    while r_curr != 0:
        # Целая часть от деления
        quotient = r_prev // r_curr

        # Обновление остатков
        r_next = r_prev - quotient * r_curr
        r_prev, r_curr = r_curr, r_next

        # Обновление коэффициентов Безу
        x_next = x_prev - quotient * x_curr
        y_next = y_prev - quotient * y_curr
        x_prev, x_curr = x_curr, x_next
        y_prev, y_curr = y_curr, y_next

    # Возвращаем НОД и коэффициенты Безу
    return abs(r_prev), x_prev, y_prev


